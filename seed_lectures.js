const https = require('https');
require('dotenv').config();

const PROJECT_REF = process.env.SUPABASE_PROJECT_REF;
const ACCESS_TOKEN = process.env.SUPABASE_ACCESS_TOKEN;

if (!PROJECT_REF || !ACCESS_TOKEN) {
  console.error('❌ Missing SUPABASE_PROJECT_REF or SUPABASE_ACCESS_TOKEN in .env');
  process.exit(1);
}

function runSQL(sql, label) {
  return new Promise((resolve, reject) => {
    const body = JSON.stringify({ query: sql });
    const options = {
      hostname: 'api.supabase.com',
      path: `/v1/projects/${PROJECT_REF}/database/query`,
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${ACCESS_TOKEN}`,
        'Content-Type': 'application/json',
        'Content-Length': Buffer.byteLength(body),
      },
    };
    const req = https.request(options, (res) => {
      let data = '';
      res.on('data', chunk => data += chunk);
      res.on('end', () => {
        if (res.statusCode >= 400) {
          console.error(`[${label}] Error (${res.statusCode}):`, data.substring(0, 300));
          reject(new Error(`HTTP ${res.statusCode}`));
        } else {
          console.log(`[${label}] ✅`);
          resolve(data);
        }
      });
    });
    req.on('error', reject);
    req.write(body);
    req.end();
  });
}

// Build INSERT SQL for lectures
function buildLectureInserts(rows) {
  const vals = rows.map(r => {
    const desc = (r.desc || '').replace(/'/g, "''");
    const title = r.title.replace(/'/g, "''");
    return `((SELECT id FROM subjects WHERE name = '${r.subject}'), ${r.cls}, ${r.ch}, '${title}', '${desc}', ${r.dur || 30}, ${r.order || r.ch})`;
  });
  return `INSERT INTO lectures (subject_id, class_level, chapter_number, title, description, duration_minutes, display_order) VALUES\n${vals.join(',\n')}\nON CONFLICT DO NOTHING;`;
}

