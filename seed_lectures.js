async function main() {
  // Clear existing lectures first
  await runSQL('DELETE FROM lectures;', 'clear-lectures');

  let total = 0;
  for (const [key, chapters] of Object.entries(lectureData)) {
    const [cls, ...subjectParts] = key.split('-');
    const subject = subjectParts.join('-');
    const classLevel = parseInt(cls);

    const rows = chapters.map((c, i) => ({
      subject: subject,
      cls: classLevel,
      ch: c.ch,
      title: c.title,
      desc: c.desc,
      dur: 30,
      order: c.ch,
    }));

    const sql = buildLectureInserts(rows);
    await runSQL(sql, `lectures-${key}`);
    total += rows.length;
  }

  console.log(`\n✅ Phase 2a complete: ${total} lectures seeded`);
}

main().catch(e => { console.error(e); process.exit(1); });
